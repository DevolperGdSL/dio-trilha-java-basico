public class Calculadora {
    /**
     * Faz a soma entre dois paramentros e retorna um inteiro
     */
    public int somar(int numeroUm, int numeroDois){
        return numeroUm + numeroDois;
    }
    /**
     * Faz a multiplicação entre dois paramentros e retorna um inteiro
     */
    public int multiplicar(int numeroUm, int numeroDois){
        return numeroUm * numeroDois;
    }
    /**
     * Faz a divisão entre dois paramentros e retorna um inteiro
     */
    public int dividir(int numeroUm, int numeroDois){
        return numeroUm / numeroDois;
    }
    /**
     * Faz a subtração entre dois paramentros e retorna um inteiro
     */
    public int subtrair(int numeroUm, int numeroDois){
        return numeroUm - numeroDois;
    }
}
